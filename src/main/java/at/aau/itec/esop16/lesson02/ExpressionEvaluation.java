public class ExpressionEvaluation {
    public static void main(String[] args) {
        int x, y;
        x = 3;
        y = 4;
        System.out.println(x == y);
        System.out.println(x != y);
        System.out.println(x >= y);
        System.out.println(x <= y);
        System.out.println(x < y);
        System.out.println(x > y);
    }
}
